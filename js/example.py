print("Enter Grade Percentage")
       
percent = input()
              
if (percent<50):
    print("F")
elif (percent>=50 and percent<60):
    print("D")
elif (percent>=60 and percent<70):
    print("C")
elif (percent>=70 and percent<80):
    print("B")

elif (percent>=80 and percent<90):
    print("A")

